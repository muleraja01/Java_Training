package basicSyntax;

public class sampleloops {
	/**
	 * @param sample loops
	 */
	public static void main(String[] args) {
		for (int i = 1; i < 10; i++) {
			System.out.println(i);
			if (i != 4) {
				System.out.println(i);
			}
		}
		int j = 0;
		while (j <= 10) {
			System.out.println(j);
			j++;

		}
	}
}
